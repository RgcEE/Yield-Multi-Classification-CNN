# Per-class cap applied to pseudo-labels at load time.
# yield_pseudolabel.py did not apply apply_class_caps correctly — Donut (134×),
# Near-full (6.6×), and Random (7.1×) all exceed the intended 3× limit.
# Cap is enforced here via random sampling (seed 42) before concatenation.
ORIG_LABELED_COUNTS = {
    "none": 117945, "Edge-Ring": 7744, "Center": 3435, "Edge-Loc": 4151,
    "Loc": 2874, "Scratch": 954, "Random": 693, "Donut": 555, "Near-full": 119,
}
MAX_PSEUDO_MULTIPLIER = 3

# Classes excluded from pseudo-labeling. These already have F1 > 0.90 in
# se-only-v1 and adding pseudo-labels provides no benefit while inflating the
# dataset. none in particular had 248,215 pseudo-samples — all passing the 3×
# cap (353,835 > 248,215) — which made the combined dataset 87% none and caused
# the model to collapse to the majority-class predictor in v1.
PSEUDO_EXCLUDED_CLASSES = {"none", "Edge-Ring", "Center", "Random", "Scratch"}


