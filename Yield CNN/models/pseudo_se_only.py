"""
Author: Reynaldo Gomez
Last Edited: 3/25/2026

Description — Pseudo-Label Run v2: SE attention only, targeted pseudo-labeling.

Extends yield_se_only.py with a larger training set assembled from two sources:

    1. Original labeled data  — ~138k samples from LSWMD.pkl (ground truth)
    2. Pseudo-labeled data     — defect-class-only samples from data/pseudo_labeled.pkl,
       generated by yield_pseudolabel.py using the best_se_only.pt checkpoint.
       Acceptance thresholds: 0.95 default, 0.999 for Donut, 0.98 for Near-full.
       Per-class cap: 3× the original labeled count for that class.

v1 failure: including none pseudo-labels (248,215 samples) pushed the combined
dataset to 87% none. The 3× cap (353,835) was never triggered since 248,215 < cap,
so all none pseudo-labels passed through uncapped. WeightedRandomSampler could not
overcome this density and the model collapsed to predicting none for everything.

v2 fix (Option B): pseudo-labeling is restricted to the 5 classes that actually
need more data based on se-only-v1 F1 scores:
    Targeted (F1 < 0.85 or very low sample count):
        Loc (0.77), Edge-Loc (0.81), Donut (~0.55), Near-full (~0.60)
    Excluded (high F1 or source-model uncertainty makes pseudo-labels unreliable):
        none (0.99), Edge-Ring (>0.90), Center (>0.90), Random (>0.90), Scratch (0.80)

Combined dataset: ~185k samples split 80/20 → ~148k train / ~37k val.
The validation set is dominated by ground-truth original labeled data.

Model, loss, optimizer, and augmentation are identical to yield_se_only.py
so that dataset composition is the only variable that changes.

Usage:
    python pseudo_se_only.py
    - trains for 40 epochs, saves best checkpoint when val loss improves
    - prints per-class classification report at the end
    - appends one row to ../results.csv
"""

