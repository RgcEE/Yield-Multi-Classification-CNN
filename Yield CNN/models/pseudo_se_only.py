def _unwrap_label(val):
    """
    LSWMD stores labels as nested arrays: [['Edge-Loc']] or [[]] for unlabeled.
    Peels nesting until it reaches the scalar string.
    Returns None for empty arrays so they can be filtered out.
    """
    while isinstance(val, (list, np.ndarray)):
        if len(val) == 0:
            return None
        val = val[0]
    return str(val).strip()


def load_pkl(pkl_path: Path):
    with pkl_path.open("rb") as f:
        df = pkl.load(f, encoding="latin-1")

    raw_labels = df["failureType"].apply(_unwrap_label)
    mask       = raw_labels.notna()
    df         = df[mask].reset_index(drop=True)
    raw_labels = raw_labels[mask].reset_index(drop=True)

    maps = []
    for wm in df["waferMap"]:
        arr = np.array(wm, dtype=np.float32)
        arr = resize(arr, (IMG_SIZE, IMG_SIZE),
                     anti_aliasing=False, preserve_range=True)
        maps.append(arr)

    X = np.stack(maps)
    y = raw_labels.values

    print(f"Loaded {len(X):,} labeled wafers across {len(set(y))} classes")
    print(pandas.Series(y).value_counts().to_string())
    return X, y


def encode_labels(y_raw):
    """
    Converts string class names to integers 0..K-1.
    Sorted alphabetically so the mapping is reproducible across runs.
    """
    classes    = sorted(set(y_raw))
    cls_to_idx = {c: i for i, c in enumerate(classes)}
    y_idx      = np.array([cls_to_idx[v] for v in y_raw], dtype=np.int64)
    return y_idx, classes


def load_pseudo(pseudo_path: Path, classes: list):
    """
    Loads pseudo_labeled.pkl and returns (X, y_idx) aligned to the class list
    established by the original labeled data.

    Only returns pseudo-labels for classes NOT in PSEUDO_EXCLUDED_CLASSES.
    Excluded classes already have high F1 in se-only-v1; adding pseudo-labels
    for them inflates the dataset without improving the weak classes.

    Verifies that the checkpoint's class list matches so integer labels are
    consistent across both sources before concatenation.
    """
    with pseudo_path.open("rb") as f:
        data = pkl.load(f)

    pseudo_classes = list(data["classes"])
    assert pseudo_classes == classes, (
        f"Class mismatch between pseudo-labeled checkpoint and original labels.\n"
        f"  Original: {classes}\n"
        f"  Pseudo:   {pseudo_classes}"
    )

    X = data["X"]
    y = data["y"].astype(np.int64)

    print(f"Loaded {len(X):,} raw pseudo-labeled samples (pre-cap, pre-exclusion)")

    # Apply per-class cap and exclude high-F1 classes.
    # The saved pkl has Donut at 134× the original count due to a missing cap
    # in yield_pseudolabel.py. Random sampling with a fixed seed is used because
    # confidence scores were not saved in the pkl.
    rng = np.random.default_rng(42)
    keep = []
    for i, cls in enumerate(classes):
        if cls in PSEUDO_EXCLUDED_CLASSES:
            print(f"  Skipping {cls} pseudo-labels (excluded — already high F1)")
            continue
        cls_idx = np.where(y == i)[0]
        cap     = ORIG_LABELED_COUNTS.get(cls, 0) * MAX_PSEUDO_MULTIPLIER
        if len(cls_idx) > cap:
            cls_idx = rng.choice(cls_idx, size=cap, replace=False)
        keep.append(cls_idx)

    keep = np.concatenate(keep) if keep else np.array([], dtype=np.int64)
    X = X[keep]
    y = y[keep]

    print(f"After exclusion + {MAX_PSEUDO_MULTIPLIER}× cap: {len(X):,} pseudo-labeled samples")
    print(pandas.Series([classes[i] for i in y]).value_counts().to_string())
    return X, y


class WaferDataset(Dataset):
    """
    PyTorch Dataset for LSWMD wafer maps.
    Augmentation (rot90 + hflip) is applied only during training.
    Defect patterns are rotationally symmetric so these are valid transforms.
    No CoordConv — wafer map is passed as a single channel (ablation Run B).
    """
    def __init__(self, X: np.ndarray, y: np.ndarray, augment: bool = False):
        self.X       = X
        self.y       = y
        self.augment = augment

    def __len__(self):
        return len(self.X)

    def _augment(self, x: torch.Tensor) -> torch.Tensor:
        k = torch.randint(0, 4, (1,)).item()
        x = torch.rot90(x, k, dims=(1, 2))
        if torch.rand(1).item() < 0.5:
            x = torch.flip(x, dims=(2,))
        return x

    def __getitem__(self, idx):
        x = torch.tensor(self.X[idx], dtype=torch.float32).unsqueeze(0)
        if x.max() > 1.5:
            x = x / 255.0
        if self.augment:
            x = self._augment(x)
        return x, int(self.y[idx])


